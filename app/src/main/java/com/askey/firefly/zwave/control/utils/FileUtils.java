    public static LinkedList<File> getAllFiles(String[] strPath) {
        LinkedList<File> files = new LinkedList<File>();
        for(int j=0;j<strPath.length;j++) {
            LinkedList<File> list = new LinkedList<File>();
            File dir = new File(strPath[j]);
            File[] file = dir.listFiles();
            for (int i = 0; i < file.length; i++) {
                if (file[i].isDirectory())
                    list.add(file[i]);
                else
                    files.add(file[i]);
            }
            File tmp;
            while (!list.isEmpty()) {
                tmp = list.removeFirst();
                if (tmp.isDirectory()) {
                    file = tmp.listFiles();
                    if (file == null)
                        continue;
                    for (int i = 0; i < file.length; i++) {
                        if (file[i].isDirectory())
                            list.add(file[i]);
                        else
                            files.add(file[i]);
                    }
                } else {
                    files.add(tmp);
                }
            }
        }
        return files;
    }
